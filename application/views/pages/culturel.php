        <h3 class="font-weight-bold">Objectifs: Transformer l’école de quartier en centre culturel saisonnier</br> Favoriser la réappropriation de l’école par le milieu.</h3>
   

        <p>Pour ce faire :</p>

            <ul>

            <li>Réaménager l’espace de l’école de façon attractive en changeant son aspect habituel</li>
            <li>Repeindre les murs dès le mois de juillet et les décorer en utilisant le talent des artistes du quartier</li>
            <li>Aménager l’espace de l’école en espaces d’activités culturelles : coins lecture, espace spectacles, stands d’exposition…</li>
            </ul>

        <h3 class="font-weight-bold">Activités : Bibliothèque</h3>
            <ul>
                <li>1 salle de livres : consultation sur place et éventuellement prêt de livres</li>
                <li>1 ou plusieurs salles de lecture publique : lectures assurées par les comédiens du quartier</li>
                <li>1 stand d’exposition vente de livres détenus par les partenaires de l’école.</li>
                <li>Initiation à l’informatique</li>
                <li>Activités pour les tous petits</li>
            </ul>

