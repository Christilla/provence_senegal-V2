            <h3 id="vacances">CENTRE CULTURELS DE VACANCES</h3>
                <p> Le manque de lecture participe de l’échec scolaire à cause de la non-maîtrise de la langue de travail à l’école, d’où il ressort selon les enseignants que les élèves ne comprennent pas les questions posées. A terme, le manque de maîtrise de la langue de travail entraîne la dévalorisation sociale et professionnelle.
                Par ailleurs, nous déplorons chaque année le désœuvrement des enfants dans les quartiers populaires, car les parents n’ont pas les moyens d’envoyer leurs enfants en colonie de vacances. A cela s’ajoute le manque d’espaces de jeux dans les quartiers.
                Or il se trouve que pendant les vacances, les écoles, souvent situées au centre des quartiers, sont inutilisées, non entretenues...
                L’idée serait donc de transformer ces écoles en centres culturels de quartier pendant l’hivernage.</p>

        </div>
</div>

