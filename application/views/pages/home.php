                        <p>
                            Côté Nord, cet afflux de pauvres hères, sans formation et parfois sans maîtrise de la langue, accentue la situation déjà tendue du marché de l’emploi. Un phénomène de rejet se fait jour de plus en plus en Europe et aux États-Unis, faisant le lit de l’extrême droite. Le phénomène de l’immigration et du racisme côté Nord ne peut trouver de solution que dans le développement du Sud, voire le co-développement, sur la base de l’intelligence et de l’amitié entre les peuples.
                        </p>
                        <p>
                            L’intelligence, c’est de comprendre pourquoi les uns sont riches et les autres pauvres, pourquoi le destin des uns dépend de celui des autres, et enfin en quoi l’inégalité et l’injustice sont à terme les véritables germes de la destruction de l’humanité.
                            </p>
                        <p>
                        Par ailleurs, étant très proches les uns des autres grâce aux technologies de l’information et de la communication, les valeurs d’égalité et de fraternité peuvent constituer un véritable moteur pour initier des projets de développement équitables, et par conséquents durables.
                    </p>
