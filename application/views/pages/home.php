                <p>La pauvreté de plus en plus intenable qui frappe les pays du Sud, due en grande partie à la détérioration des termes de l’échange depuis près d’un siècle avec les anciennes puissances coloniales et la faiblesse des transferts dits « de coopération » du Nord vers le Sud, accentue le fossé économique entre ces deux pôles.
                Par ailleurs, le monde n’a jamais été aussi interconnecté, grâce au développement prodigieux des nouvelles technologies de l’information et de la communication.</p>
                <p> Les grands médias internationaux étant aux mains des grandes puissances occidentales, les peuples du sud vivent chaque jour l’actualité des pays du Nord, subissent leur influence, et constatent par l’image la richesse du Nord. Les émigrés aux États-Unis et en Europe quant à eux, aiment à montrer l’amélioration de leurs conditions de vie auprès de leur entourage resté au pays, entretenant ainsi le mythe de l’Eldorado, ce qui provoque une puissante émulation à l’émigration. Les jeunes ont pour seule obsession d’émigrer, pour sortir à tout prix de la misère.</p>
