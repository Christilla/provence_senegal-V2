    <p class="font-italic">Le Sénégal est un pays passionnant. De Saint-Louis à Dakar, de Dakar à Ziguinchor, c’est une multitude de paysages de mer et de brousse, de savane sèche et de forêts au Sud.</p>
    A Saint-Louis, l’histoire coloniale transpire par toutes les façades des maisons qui ont abrité la société saint-louisienne, composées de Noirs, de Blancs et de métis.
    A Dakar, c’est un mélange de riches demeures et de baraques de populations issues des migrations rurales.
    Gorée est l’île du souvenir, l’île du pardon et du non-oubli. L’esclavage en fait son renom.</p>

