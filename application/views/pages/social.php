<p>
    1er cas : Awa Ndiaye est une mère de famille qui habite un quartier de Dakar nommé la Gueule Tapée.
    Son mari, handicapé pendant de nombreuses années, est mort récemment. Awa Ndiaye a sept ou huit enfants. Elle habite une baraque sans eau, où survivent une dizaine de familles. Elle achète chaque matin une dizaines de kilos de fruits au marché qu’elle revend au détail, assise devant un étal. Elle récolte ainsi 1 euros par jour, et fait vivre ses enfants de cette somme plus que dérisoire.

    2eme cas : Mame Fatou Paye est une jeune femme d’une quarantaine d’années, qui a perdu son mari il y a deux ans, emporté par une crise d’asthme.
    Elle a 3 enfants scolarisés. Elle se retrouve sans revenus. Elle gère depuis plusieurs années une tontine de femmes du quartier. Le produit de cette tontine permet aux femmes d’améliorer les revenus de leur famille.

    3eme cas : Marie Sène a perdu son mari et sa fille en 1999.
    Ses enfants sont obligés d’abandonner l’école et d’entrer en apprentissage (tailleur, maçon, ébéniste)… L’un deux est mort d’un accident de travail en 2004. La famille n’a pas été indemnisée. Ce décès a mis la famille dans une situation catastrophique. Marie ne peut plus assurer l’éducation des plus petits. Leur scolarité est compromise.

    4eme : Ramata a 5 enfants.
    Elle a perdu son logement et tous ses biens dans un incendie qui avait dévasté le quartier de Fass paillote. Toutes les familles sinistrées ont été relogées dans des salles de classes de l’école Manguiers. A la rentrée des classes, ces familles ont été obligées de se débrouiller. Actuellement, Ramata a beaucoup de difficultés à payer le loyer d’une chambre à Ouagou Niayes. Elle est actuellement réduite à la mendicité pour nourrir ses enfants.

    Toutes ces femmes pourraient bénéficier un fonds de départ (de l’ordre de 75 euros) pour démarrer une activité génératrice de revenus.
    Ce fonds de départ, remboursé, pourra aider d’autres femmes.</p>

  
    <h3>UN REPAS POUR UN ENFANT MALADE :</h3>
