  <p>
    L’hôpital Albert Royer de Dakar est le seul hôpital spécialisé dans les pathologies pédiatriques.
    Il était il y a encore 10 ans financé par la coopération canadienne. Mais depuis le retrait du Canada, l’hôpital n’a plus les moyens de nourrir convenablement les petits malades...

    A l’hôpital de Fann, des enfants, venus de l’extérieur du pays avec leurs mamans, sont nourris de riz cuit à l’eau sur lequel est posé un petit morceau de poisson séché.
    Le déficit en glucides, protides et vitamines ne favorisent pas leur rétablissement.
    Les mamans, souvent démunies, et ne connaissant personne à Dakar se contentent de cacahuètes grillées vendues 4 centimes d’euros à la porte de l’hôpital.

    L’idée serait de cuisiner chez Maguette Samb, la maman de Diéo, qui habite à 500 m de là, des repas complets traditionnels, comme le tiéboudiène (riz au poisson avec tomate et légumes) ou le mafé (riz à la viande sauce arachide et légumes) dans des marmites géantes (disponibles) pour 100 à 200 personnes. Ces repas seraient servis à midi pour les enfants hospitalisés et leurs accompagnants. Les services sociaux de l’hôpital sont d’ores et déjà partie prenante pour lister les bénéficiaires.

    Coût : 30 à 40 centimes d’euros par personnes, soit 60 à 80 euros par jour compter les rétributions des cuisinières (2 euros X 3 par jour) et du chauffeur (10 euros par semaine)
